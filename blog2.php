        <h2>Introduction</h2>
        <p>Happy Folks Club had conducted its very own ‘Graduation Ceremony’ for the young-hearted senior citizens on the 4th of August, 2024 at ISBR Business School. Our beloved graduates were celebrated for their successful and memorable Swatantra journey.</p>

        <h2>Inauguration</h2>
        <p>The ceremony started off with a heart-warming ‘welcome speech’ by Harshita Bhansali and Siddharth Tandle, congratulating the graduating batch for their achievements and presenting the flow of events to the gathering. To enlighten the spirit among the audience, the ‘Lighting of the Lamp’ by the dignitaries took place followed by a soliciting prayer led by Ravela Martin.</p>

        <h2>Memories of Swatantra Journey</h2>
        <p>We then had Siddharth Kothari who gave a speech about the Swatantra report. The Happy Folks Club conducted its first summit on 12th May, 2024, which was Mother’s Day. Swatantra began its first day on 16th June, 2024, which was Father’s Day. The graduation also was on Friendship Day. Though it was a clear coincidence, it seemed as though it was a part of the universe’s plan. The excited faces of our senior citizens as they were beginning a new journey on the first day of Swatantra, was a moment of cuteness overloaded.</p>
        <p>They were reminiscing their memories of their participation in the activities, the friendly competitions they had with their fellow citizens, being part of various insightful talks and much more. Dr. Manish Kothari, President of ISBR, spoke about how this journey took sacrifices, hard work and sheer passion for the cause. It was a journey that almost seemed impossible to be executed, but with the help of our mentors, Ms. Veena, Mr. Mohan and Mr. Chella, Swatantra was a marvelous journey filled with joy and fun.</p>

        <h2>Chief Guest and Guest of Honor</h2>
        <p>The Chief Guest for the day was Mr. Sanjeev Kumar Gupta, CEO of Karnataka Digital Economy Mission. He expressed how delighted he was when he learned about Happy Folks Club and its mission. He mentioned wholeheartedly that if he could present our Club with the Padmashree award, he would be happy in doing so. We also had Ms. Lakshmi Pratury, CEO of INK, as our Guest of Honor. Her motivational words and insightful knowledge uplifted the essence of the program.</p>

        <h2>Award Ceremony</h2>
        <p>The next event of the day was the distribution of certificates and scrolls to graduates. Our senior citizens were felicitated for their achievements throughout their Swatantra journey. It was wholesome to watch them collect their certificates as they felt like a young student again. It was very special for them as their families were clapping and applauding them for their graduation, as they did the same for them years before. A special list of toppers was also presented, consisting of graduates who had been involved in their Swatantra events actively, which helped them score some extra points.</p>

        <h2>Future of Happy Folks Club</h2>
        <p>After the cheerful award ceremony, we had Siddharth Kothari speak about the future of Swatantra, where he highlighted the key importance of this initiative and how it is a movement. He also credited the amazing Happy Folks Club team which consisted of Vineet Jain, Mudit R, Harshita Bhansali, Ravela Martin and Siddharth Tandle. It revealed to the audience how this initiative is a responsibility and how important it is to address this cause to society.</p>

        <h2>Conclusion</h2>
        <p>The graduation program came to an end with the vote of thanks, expressing gratitude to ISBR Business School, Rotary Bangalore, Yes Bank, ISBR Cares, Elcia, Sustainable Development Goals (SDG), and Inclusive for their immense contribution in making the Graduation Day a true success and a special one for our senior citizens.</p>
